var id =["Arif", "Bijoy", "Ashraf", "Ashim"];
console.log(id);

//rearrange data by alphabatic
id.sort();
console.log(id);

//rearrange data by reverse alphabatic
id.reverse();
console.log(id);

//remove by last
id.pop();
console.log(id);

//add data by last
id.push("Manik");
console.log(id);


//remove data from first
id.shift();
console.log(id);

//add data to first
id.unshift("Shohel","Hamim");
console.log(id);



